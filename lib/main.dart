class MyHomePage extends StatefulWidget {
  const MyHomePage({super.key, required this.title});

  // This widget is the home page of your application. It is stateful, meaning
  // that it has a State object (defined below) that contains fields that affect
  // how it looks.

  // This class is the configuration for the state. It holds the values (in this
  // case the title) provided by the parent (in this case the App widget) and
  // used by the build method of the State. Fields in a Widget subclass are
  // always marked "final".

  final String title;

  @override
  State<MyHomePage> createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  final _flutterMidi = FlutterMidi();

  void load() async {
    if (kIsWeb) {
      _flutterMidi.prepare(sf2: null);
    } else {
      // const asset = 'assets/Piano.sf2';
      const asset = 'assets/YDP-GrandPiano-20160804.sf2';
      _flutterMidi.unmute();

      ByteData bytes = await rootBundle.load(asset);
      _flutterMidi.prepare(sf2: bytes, name: asset.replaceAll('assets/', ''));
    }
  }

  @override
  void initState() {
    load();
    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: Center(
        child: InteractivePiano(
          highlightedNotes: [NotePosition(note: Note.C, octave: 3)],
          naturalColor: Colors.white,
          accidentalColor: Colors.black,
          keyWidth: 60,
          noteRange: NoteRange.forClefs([
            Clef.Treble,
          ]),
          onNotePositionTapped: (position) {
            final pitch = position.pitch;
            _flutterMidi.playMidiNote(midi: pitch);
          },
        ),
      ),
    );
  }
}
